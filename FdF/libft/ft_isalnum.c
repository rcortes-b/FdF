#include "libft.h"

int	ft_isalnum(int c)
{
	if (((char)c >= '0' && (char)c <= '9')
		|| ((char)c >= 'a' && (char)c <= 'z')
		|| ((char)c >= 'A' && (char)c <= 'Z'))
		return (1);
	return (0);
}
